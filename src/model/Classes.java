package model;

public class Classes {

	private String id;
	private String name;
	private String shortclasses;
	private int partner_id;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getShortclasses() {
		return shortclasses;
	}
	public void setShortclasses(String shortclasses) {
		this.shortclasses = shortclasses;
	}
	public int getPartner_id() {
		return partner_id;
	}
	public void setPartner_id(int partner_id) {
		this.partner_id = partner_id;
	}
		
}
